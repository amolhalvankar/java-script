//Logical Operator
let a = 5 ;
let b = 4 ;

//Logical &&
con1 = a > b ;
con2 = a == 5 ;
console.log("con1 && con2" , con1 && con2); //true

//Logical ||
con1 = a > b ;
con2 = a == 2 ;
console.log("con1 || con2" , con1 || con2); //true

//Logical NOT !
con1 = a > b ;
con2 = a == 5 ;
console.log("!(con2)" , !(con2)); //true, but it gives false




