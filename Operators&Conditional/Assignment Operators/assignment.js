// Assignment Operators


let a= 5 ;

a += 2 ;   // a = a + 4 
console.log("a = " , a); // 7

a -= 2 ;   // a = a - 2 
console.log("a = " , a); 

a *= 2 ;   // a = a * 2 
console.log("a = " , a); 

a /= 2 ;   // a = a / 2 
console.log("a = " , a); 

a **= 2 ;   // a = a ** 2 
console.log("a = " , a);

a %= 2 ;   // a = a % 2
console.log("a = " , a);



